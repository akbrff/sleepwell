    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nomor");
        model.addColumn("Tipe");
        model.addColumn("Harga");
        model.addColumn("Status");

        try {
            Connection conn = koneksi.getkoneksi();
            // Mencari nomor kamar, tipe, atau statusnya
            String sql = "SELECT * FROM kamar WHERE nomor_kamar LIKE ? OR tipe_kamar LIKE ? OR status_kamar LIKE ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            String keyword = "%" + jTextField5.getText() + "%";
            pst.setString(1, keyword);
            pst.setString(2, keyword);
            pst.setString(3, keyword);
            
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_kamar"),
                    rs.getString("nomor_kamar"),
                    rs.getString("tipe_kamar"),
                    rs.getString("harga_per_malam"),
                    rs.getString("status_kamar")
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

