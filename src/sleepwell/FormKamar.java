        // TODO add your handling code here:
        if (jTextField1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data dari tabel terlebih dahulu yang ingin diubah!");
            return;
        }

        String nomorKamar = jTextField2.getText().trim();
        String tipeKamar = (jComboBox2.getSelectedItem() != null) ? jComboBox2.getSelectedItem().toString() : "";
        String hargaKamar = jTextField3.getText().trim();
        String statusKamar = (jComboBox1.getSelectedItem() != null) ? jComboBox1.getSelectedItem().toString() : "";

        if (nomorKamar.isEmpty() || tipeKamar.isEmpty() || hargaKamar.isEmpty() || statusKamar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua data kamar wajib diisi/dipilih!");
            return;
        }

        try {
            String sql = "UPDATE kamar SET nomor_kamar=?, tipe_kamar=?, harga_per_malam=?, status_kamar=? WHERE id_kamar=?";
            Connection conn = koneksi.getkoneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, nomorKamar);
            pst.setString(2, tipeKamar);
            pst.setString(3, hargaKamar);
            pst.setString(4, statusKamar);
            pst.setString(5, jTextField1.getText()); // WHERE id_kamar

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Kamar Berhasil Diubah");
            tampilData();
            clearForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
